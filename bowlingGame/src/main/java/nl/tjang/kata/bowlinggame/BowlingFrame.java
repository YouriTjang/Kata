package nl.tjang.kata.bowlinggame;


public class BowlingFrame {
    int first;
    int second;

    public BowlingFrame() {
    }

    public BowlingFrame(int first) {
        this.first = first;
    }

    public int getFirst() {
        return first;
    }

    public void setFirst(int first) {
        this.first = first;
    }

    public int getSecond() {
        return second;
    }

    public void setSecond(int second) {
        this.second = second;
    }
}
